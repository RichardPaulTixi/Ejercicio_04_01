    private void ActualizarTabla(){
        String [] encabezado=new String[6]; 
        encabezado[0]="Calificacion";
        encabezado[1]="Promedio";
        encabezado[2]="Codigo Estudiante";
        encabezado[3]="Codigo Asignatura";
        encabezado[4]="Asignatura";
        encabezado[5]="Estudiante";
        
        var datos= new Object[this.calificacionControl.listarCal().size()][6];
        
        var i=0;
        for(var calificacion:this.calificacionControl.listarCal()){
            datos[i][0]=calificacion.getCalificacion();
            datos[i][1]=calificacion.getPromedio();
            datos[i][2]=calificacion.getCodigoEst();
            datos[i][3]=calificacion.getCodigoAsig();
            datos[i][4]=calificacion.getAsignatura().getNombre();
            datos[i][5]=calificacion.getEstudiante().getNombre();
            i++;
        }
        this.modeloTabla=new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
    }
